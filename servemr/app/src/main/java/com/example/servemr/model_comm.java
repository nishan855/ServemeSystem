package com.example.servemr;

public class model_comm {
    String bids,dt,name,tm,tskid,uid;

    public model_comm() {
        this.bids = bids;
        this.dt=dt;
        this.name=name;
        this.tm=tm;
        this.tskid=tskid;
        this.uid=uid;
    }

    public String getBids() {
        return bids;
    }

    public void setBids(String bids) {
        this.bids = bids;
    }

    public String getDt() {
        return dt;
    }

    public void setDt(String dt) {
        this.dt = dt;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getTm() {
        return tm;
    }

    public void setTm(String tm) {
        this.tm = tm;
    }

    public String getTskid() {
        return tskid;
    }

    public void setTskid(String tskid) {
        this.tskid = tskid;
    }

    public String getUid() {
        return uid;
    }

    public void setUid(String uid) {
        this.uid = uid;
    }
}
